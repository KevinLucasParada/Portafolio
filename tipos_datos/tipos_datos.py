# Ejemplos de tipos de datos en Python

# Entero
edad = 25
print("Edad:", edad, "| Tipo:", type(edad))

# Flotante
altura = 1.75
print("Altura:", altura, "| Tipo:", type(altura))

# Cadena de texto (string)
nombre = "Kevin"
print("Nombre:", nombre, "| Tipo:", type(nombre))

# Booleano
es_estudiante = True
print("Â¿Es estudiante?", es_estudiante, "| Tipo:", type(es_estudiante))

# Lista
colores = ["rojo", "azul", "verde"]
print("Colores:", colores, "| Tipo:", type(colores))

# Tupla
dimensiones = (1920, 1080)
print("Dimensiones:", dimensiones, "| Tipo:", type(dimensiones))

# Diccionario
persona = {"nombre": "Kevin", "edad": 25}
print("Persona:", persona, "| Tipo:", type(persona))
